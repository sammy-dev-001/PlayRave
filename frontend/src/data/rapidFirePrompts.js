const getRandomQuestion = (category = 'normal', usedQuestions = []) => {
    const categoryQuestions = rapidFirePrompts[category] || rapidFirePrompts.normal;
    const available = categoryQuestions.filter(q => !usedQuestions.includes(q));

    if (available.length === 0) {
        return categoryQuestions[Math.floor(Math.random() * categoryQuestions.length)];
    }

    return available[Math.floor(Math.random() * available.length)];
};

